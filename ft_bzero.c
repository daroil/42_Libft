#include "libft.h"

int ft_bzero(char* s, size_t n)
{
    size_t i;

    if (!s)
        return(0);
    i=0;
    while(i!=n && s[i] != '\0')
    {
        s[i] = 0;
        i++;
    }
    return(0);
}