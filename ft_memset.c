#include "libft.h"

char* ft_memset(char* s, char c, size_t len)
{
    size_t i;

    if (!s)
        return(0);
    i=0;
    while(i!=len && s[i] != '\0')
    {
        s[i] = c;
        i++;
    }
    return(s);
}