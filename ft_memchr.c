#include "libft.h"

char* ft_memchr(char *s1, char c, size_t n)
{
	size_t i;
	
	i = 0;		
	while(s1[i] != '\0' && i < n)
	{
		if (s1[i] == c)
			return(&s1[i]);
		i++;	
	}
	return(NULL);
}