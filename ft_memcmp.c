#include "libft.h"

int ft_memcmp(char *s1, char *s2, size_t n)
{
	size_t	i;

	i = 0;
	while (i < n)
	{
		if (s1[i] != s2[i])
			break;
		i++;		
	}
	return(s1[i] - s2[i]);
}
