#include "libft.h"

char* ft_memcpy(char* dst, char* src, size_t n)
{
    size_t i;
    
    i=0;
    while(src[i]!='\0' && i<=n)
    {
        dst[i]=src[i];
        i++;
    }
    return(dst);
}